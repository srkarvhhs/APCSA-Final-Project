import java.io.*;
import java.util.*;
import java.text.*;
import java.math.*;
import java.util.regex.*;

public class Solution {
    public static void main(String[] args) {
        int n = 0;
   Scanner scan = new Scanner(System.in);
  while(scan.hasNext() == true){
      n++;
      String s = scan.nextLine();
      System.out.println(n + " " +s);
  }
    }
}
